                             <h2> Por onde começar? </h2>
                            <p> Primeiramente, os grupos devem ser formados, contendo todos os membros
                            necessários: Diretor, Roteirista, Diretor de Arte, Editor, Atores e Diretor de Fotografia.
                            Após a formação do grupo, é importante discutir as ideias de como será a história passada
                            pelo curta-metragem e o que deve ser feito por cada integrante. Depois de discutirem os principais
                            tópicos para produção do trabalho, o roteirista deve começar a desenvolver o roteiro do curta,
                            seguindo a formatação adequada, disponível na aba "Downloads e Formatação".
                            </p>

                            <p> O próximo passo é planejar a arte do curta-metragem: cenários, figurinos, cores, maquiagens...
                            Isso deve ser realizado pelo Diretor de Arte, que deve entregar uma pasta com as principais insipirações 
                            e ideias relacionadas à parte visual, conforme exemplos também disponibilizados na aba mencionada.
                            </p>

                            <h2> Iniciando as gravações... </h2>
                            <p> O cenário e os atores devem ser caracterizados de acordo com a de pasta concepção de arte.
                            É importante fotografar inicialmente o cenário e caracterização dos personagens, para atualizar
                            a pasta de arte ao decorrer da produção do curta.
                            </p>
                            <p> Uma dica para a filmagem é desenhar o cenário e visualizar os melhores ângulos para
                            que o curta se torne o mais agradável visualmente possível, além de sempre considerar a iluminação
                            necessária para a produção uma boa cena, de acordo com o as ideias discutidas.
                            </p>
                            <p> Outra dica para as gravações é sempre, ao iniciar a filmagem da cena, dizer o nome da cena e a
                            tomada, para facilitar o trabalho do editor. Dessa forma, deve ser anotado qual tomada de cada cena será
                            utilizada no curta e quais serão descartadas. Além disso, o uso da claquete é de grande ajuda para a 
                            sincronização de áudio e som.
                            </p>
                            <p> A qualidade do som é um dos fatores determinantes para um bom curta.
                            Por isso, é necessário que o cenário esteja em silêncio na hora das gravações. O uso de microfones separados, como
                            o microfone de lapela, para gravar a voz dos atores é um ótimo recurso. Utilizar o celular de forma escondida
                            para gravar a fala dos personagens durante a cena é uma técnica que pode ser aproveitada. 
                            Lembrando que a sincronização de áudio com a imagem, nesse caso, deve ser feita de forma cuidadosa na edição.
                            </p>

                            <h2> Edição </h2>
                            <p> A edição é uma parte importantíssima na produção do curta, pois é através dela que o audiovisual
                            ganha sua forma. Aqui se decide a paleta de cores e outros efeitos, a trilha sonora, as transições
                            entre as cenas, entre outros detalhes que farão toda diferença, de acordo com a forma de utilização. 
                            </p>
                            <p> No ano de 2019, a aluna e bolsista do CineArte, Anna Letícia De Cesero, apresentou uma oficina sobre edições, disponível no link:
                                <a href=https://drive.google.com/file/d/1G58k_U40r5L1_qI-FzpSenBkpi5P-8dq/view?usp=sharing> Montagem Audiovisual</a>
                            </p>
                            <p> É importante lembrar que o que for utilizado no curta-metragem deve ser livre de direitos autorais, como
                            as músicas, por exemplo.
                            </p>
